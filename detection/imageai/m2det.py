from skimage.io import imshow, imread, imsave


path_to_images_folder = '/Users/user/Desktop/aylifind/detection/cats_original/'


def make_detection(i, image_path):
    
    image = imread(image_path)
    # copy to draw on
    draw = image.copy()
    draw = cv2.cvtColor(draw, cv2.COLOR_BGR2RGB)

    # preprocess image for network
    image = preprocess_image(image)
    image, scale = resize_image(image)

    # process image
    start = time.time()
    boxes, scores, labels = model.predict_on_batch(np.expand_dims(image, axis=0))
    print("processing time: ", time.time() - start)

    # correct for image scale
    boxes /= scale

    # visualize detections
    for box, score, label in zip(boxes[0], scores[0], labels[0]):
        # scores are sorted so we can break
        if score < 0.5:
            break

        color = label_color(label)
        try:
            b = box.astype(int)
            draw_box(draw, b, color=color)

            caption = "{} {:.3f}".format(labels_to_names[label], score)
            draw_caption(draw, b, caption)
            x1 = b[0]
            y1 = b[1]
            x2 = b[2]
            y2 = b[3]
            img = image[y1:y2, x1:x2]
        except:
            continue
    imsave('/Users/user/Desktop/aylifind/detection/m2det_e/' + 'image_{}.png'.format(i), img)
    

for i, image in enumerate(os.listdir(path_to_images_folder)):
    if image.endswith('.jpg'):
        make_detection(i, path_to_images_folder + image)
